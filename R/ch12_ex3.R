#'Regression Example - Motor Skills
#'
#'An example dataset used in Chapter 12 of the book 
#'\emph{Introduction to the New Statistics}.
#'
#'The data were collected in a large online study using Mechanical Turk participants.
#'Participants completed 3 mirror tracing trials. In each trial they received a score between 0 and 1
#'representing the proportion of tracing within the line of the target. A composite score was computed
#'by averaging the scores from three trials.
#'
#'@source
#'Cusack, M., Vezenkova, N., Gottschalk, C., & Calin-Jageman, R. J. (2015). Direct and Conceptual Replications of Burgmer & Englich (2012):
#'Power May Have Little to No Effect on Motor Performance. \emph{PLOS ONE, 10},
#'e0140806. \url{http://doi.org/10.1371/journal.pone.0140806}
#'
#'@format A data frame with 210 rows and 3 variables:
#'\describe{
#'  \item{age}{Age of participant}
#'  \item{motor_skill}{Motor skill tracing composite score}
#'}
#'
#'@references
#'Cumming, G., & Calin-Jageman, R. (2017).
#'  \emph{Introduction to the New Statistics}. New York; Routledge.
